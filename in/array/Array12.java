package in.array;
import java.util.Scanner;
public class Array12 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n= sc.nextInt();
        String[] a= new String[n];
        for(int i=0;i<a.length;i++){
            System.out.println(a[i]);
        }
    }
}
